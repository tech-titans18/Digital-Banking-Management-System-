    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String username = txt_username.getText();
        char[] password = txt_password.getPassword();
        String sql = "SELECT * FROM open_account WHERE username='"+username+"' AND userpassword='"+String.valueOf(password)+"'; ";
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            //Copy the url of database connection from connection properties
            String url = "jdbc:mysql://localhost:3306/bank?zeroDateTimeBehavior=CONVERT_TO_NULL";
            String sqlusername = "bank";//username of MySQL Connection
            String sqlpassword = "1234";//password of MYSQL Connection
            //Register the Connection
            Connection conn = DriverManager.getConnection(url, sqlusername, sqlpassword);
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "Invalid username and password");
                
            }else{
                JOptionPane.showMessageDialog(null, "Login Successful");
                new MainMenu().show();
                String name = rs.getString(8)+" "+rs.getString(7);
                int account_number = rs.getInt(2);
                MainMenu.lbl_name.setText(name);
                MainMenu.lbl_accountnumber.setText(Integer.toString(account_number));
                this.dispose();
            }
            
        }catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

